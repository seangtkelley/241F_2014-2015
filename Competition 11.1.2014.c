void raisearm(int ticks)
{
	SensorValue[pn] = 0;//release
	while(SensorValue[armP]<ticks)
	{
		motor[TLarm]=118;
		motor[TRarm]=118;
		motor[BLarm]=118;
		motor[BRarm]=118;
	}
	motor[TLarm]=0;
	motor[TRarm]=0;
	motor[BLarm]=0;
	motor[BRarm]=0;
	SensorValue[pn] = 1;//lock
}

