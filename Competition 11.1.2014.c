void increment(int lev) //raising the arm to certain levels
{
		int level[]={1322,1685,1965,2235,2615,3030,1270};
    if(SensorValue[armP]<level[lev])
    {
        raisearm(level[lev]);
    }
}

void taketower()
{
				int level[]={1322,1685,1965,2235,2615,3030,1270};
        backward();
        increment(level[6]);
        forward();
        backward();
}


void drive(bool half)
{
		if (half==false)
		{
			motor[BR] = vexRT[Ch2];
			motor[FR] = vexRT[Ch2];
			motor[BL] = vexRT[Ch3];
			motor[FL] = vexRT[Ch3];
		}
		else if(half ==true)
		{
			motor[BR] = vexRT[Ch2]/3;
			motor[FR] = vexRT[Ch2]/3;
			motor[BL] = vexRT[Ch3]/3;
			motor[FL] = vexRT[Ch3]/3;
		}
}

void lock()
{
	SensorValue[pn]=1;
}
void release()
{
	SensorValue[pn]=0;
}

