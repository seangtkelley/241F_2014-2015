task usercontrol()
{
	bool half=false;
	int buttonToggleState8r = 0;//half speed
  int buttonPressed8r = 0;
	int buttonToggleState7l = 0;//pneumatics
  int buttonPressed7l = 0;

	startTask(armcontrol);


	while (true)
{


//Toggle half speed---------------------------------------------------------------
		if( vexRT[ Btn7L ] == 1 )
  {
      if( ! buttonPressed7l )
      {
      	// change the toggle state
      	buttonToggleState7l = 1 - buttonToggleState7l;

        // Note the button is pressed
        buttonPressed7l = 1;
      }
  }
  else
  {
	  // the button is not pressed
	   buttonPressed7l = 0;
	}
  // Now do something with our toggle flag
  if( buttonToggleState7l )
  {
      half=true;
  }
  else
  {
      half=false;
	}






//Toggle pneumatics------------------------------------------------
	if( vexRT[ Btn8R ] == 1 )
  {
      if( ! buttonPressed8r )
      {
      	// change the toggle state
      	buttonToggleState8r = 1 - buttonToggleState8r;

        // Note the button is pressed
        buttonPressed8r = 1;
      }
  }
  else
  {
	  // the button is not pressed
	   buttonPressed8r = 0;
	}
  // Now do something with our toggle flag
  if( buttonToggleState8r )
  {
      SensorValue[pn]=1;
  }
  else
  {
      SensorValue[pn]=0;
	}




	//DRIVE-----------------------------------------------------


		drive(half);
		motor[leftArm] = 0;
  	motor[rightArm] = 0;
		motor[leftintake] = 0;
  	motor[rightintake] = 0;


  	//ARM MOTION----------------------------


  	while(vexRT[Btn6D] == 1)
		{
			motor[leftArm] = 118;
  		motor[rightArm] = 118;
  		drive(half);
		}

		while(vexRT[Btn5D] == 1)
		{
			motor[leftArm] = -118;
  		motor[rightArm] = -118;
  		drive(half);
		}

		while(vexRT[Btn5U] == 1)
		{
			motor[leftintake] = 118;
  		motor[rightintake] = 118;
  		drive(half);
		}
		while(vexRT[Btn6U] == 1)
		{
			motor[leftintake] = -118;
  		motor[rightintake] = -118;
  		drive(half);
		}




	}
}
